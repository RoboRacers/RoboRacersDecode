package org.firstinspires.ftc.teamcode.PostLobsterCup.utilities;

public class FloatPair {
    private float min;
    private float max;

    public FloatPair(float min, float max)
    {
        this.min = min;
        this.max = max;
    }

    public float getMax() {
        return max;
    }

    public float getMin() {
        return min;
    }
}
