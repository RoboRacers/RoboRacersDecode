package org.firstinspires.ftc.teamcode.PostLobsterCup.utilities;

public class DoublePair {
    private double min;
    private double max;

    public DoublePair(double min, double max)
    {
        this.min = min;
        this.max = max;
    }

    public double getMin() {
        return min;
    }

    public double getMax() {
        return max;
    }
}
