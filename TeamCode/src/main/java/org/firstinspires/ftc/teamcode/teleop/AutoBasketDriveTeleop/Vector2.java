package org.firstinspires.ftc.teamcode.teleop.AutoBasketDriveTeleop;

/**
 * Simple 2D vector class for pathing.
 */
public class Vector2 {
    public double x, y;

    public Vector2(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
